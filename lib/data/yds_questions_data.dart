/// Kategori → Faz konusu eşlemesi
String _mapCategoryToTopic(String category) {
  switch (category) {
    case 'Tenses':
    case 'Modals':
      return 'Zamanlar ve Modallar';
    case 'Conjunctions':
      return 'Bağlaçlar';
    case 'Prepositions':
    case 'Vocabulary':
      return 'Edatlar ve Kelime Bilgisi';
    case 'Passive Voice':
    case 'Pronouns/Quantifiers':
    case 'Relative Clauses':
      return 'Dilbilgisi Bütünlüğü';
    // Faz 2
    case 'Cloze Test':
      return 'Cloze Test';
    case 'Sentence Completion':
      return 'Cümle Tamamlama';
    case 'Translation':
      return 'Çeviri';
    // Faz 3
    case 'Reading':
    case 'Reading Comprehension':
      return 'Paragraf Soruları';
    case 'Dialogue':
    case 'Restatement':
    case 'Restatement (Closest Meaning)':
      return 'Diyalog ve Yakın Anlam';
    case 'Paragraph Completion':
    case 'Irrelevant Sentence':
      return 'Anlam Bütünlüğü';
    default:
      return category;
  }
}

/// Harf → index (A=0, B=1, C=2, D=3, E=4)
int _letterToIndex(String letter) {
  switch (letter) {
    case 'A': return 0;
    case 'B': return 1;
    case 'C': return 2;
    case 'D': return 3;
    case 'E': return 4;
    default: return 0;
  }
}

